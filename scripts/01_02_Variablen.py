"""Deklaration von Variablen.

Die Deklaration und Nutzung von Variablen ist ein essentieller Bestandteil der
Programmierung. In diesem Skript wird kurz erläutet, wie Variablen angelegt und
genutzt werden.

"""

# Die Zuweisung eines Wertes zu einer Variablen erfolgt über den Opterator '='.
var = 2

# Rechnen mit Variablen.
var2 = var * 2 # var2 hält den Wert 4

# Manipulation einer Variablen.
# Möchte man den Wert einer Variablen verändern (x = x * 2) bietet Python die
# folgende Syntax. var muss in diesem Fall nur einmal aufgerufen werden.
var *= 2 # auch +=, -=, /=
